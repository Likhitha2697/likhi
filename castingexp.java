package com.may15;

public class castingexp 
{
public static void main(String[] args)
{   //implicit casting
	byte a=10;
	int b=a;
	System.out.println(b);
	//explicit conversion
	int c=10;
	byte d=(byte)c;
	System.out.println(d);
	float e=10.45f;
	int f=(int)e;
	System.out.println(f);
}
}
